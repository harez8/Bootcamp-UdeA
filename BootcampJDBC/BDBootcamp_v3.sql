CREATE DATABASE IF NOT EXISTS bootcamp_v3
DEFAULT CHARACTER SET utf8mb4
COLLATE utf8mb4_general_ci;
USE bootcamp_v3;

DROP TABLE IF EXISTS inscripcion;
DROP TABLE IF EXISTS campista;
DROP TABLE IF EXISTS grupo;
DROP TABLE IF EXISTS sede;
DROP TABLE IF EXISTS instructor;
DROP TABLE IF EXISTS bootcamp;

CREATE TABLE sede (
  id_sede INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(45) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE bootcamp (
  id_bootcamp INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(50) NOT NULL,
  nivel ENUM('Básico', 'Intermedio', 'Avanzado') NOT NULL
) ENGINE=InnoDB;

CREATE TABLE instructor (
  id_instructor INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(100) NOT NULL,
  correo VARCHAR(100) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE grupo (
  id_grupo INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(50),
  fk_bootcamp INT,
  fk_sede INT,
  fk_instructor INT,
  FOREIGN KEY (fk_sede) REFERENCES sede(id_sede),
  FOREIGN KEY (fk_bootcamp) REFERENCES bootcamp(id_bootcamp),
  FOREIGN KEY (fk_instructor) REFERENCES instructor(id_instructor)
) ENGINE=InnoDB;

CREATE TABLE campista (
  id_campista INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(100) NOT NULL,
  documento VARCHAR(20) NOT NULL,
  genero_nacimiento ENUM('M', 'F'),
  activo BOOLEAN DEFAULT TRUE
) ENGINE=InnoDB;

CREATE TABLE inscripcion (
  id_inscripcion INT AUTO_INCREMENT PRIMARY KEY,
  fk_campista INT,
  fk_grupo INT,
  fecha_registro DATE,
  FOREIGN KEY (fk_campista) REFERENCES campista(id_campista),
  FOREIGN KEY (fk_grupo) REFERENCES grupo(id_grupo)
) ENGINE=InnoDB;

